#include "libft.h"

int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i])
		i++;
	if ((s1[i] - s2[i] == 0) && ft_strlen(s1) == ft_strlen(s2))
		return (0);
	else
		return (1);
}
